class User {
  final String id;
  final String name;
  final String email;
  final String deviceName;
  final String avatarUrl;
  final String websiteUrl;

  const User({
    required this.id,
    required this.name,
    required this.email,
    required this.deviceName,
    required this.avatarUrl,
    required this.websiteUrl,
  });
}
